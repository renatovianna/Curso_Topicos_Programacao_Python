preco = int(input('Qual a categoria do produto de acordo com o preço?\n'))

if preco == 10:
    print('Categoria 1')
elif preco == 15:
    print('Categoria 2')
elif preco == 25:
    print('Categoria 3')
elif preco == 30:
    print('Categoria 4')
elif preco == 40:
    print('Categoria 5')
else:
    print('Valor inválido.')
